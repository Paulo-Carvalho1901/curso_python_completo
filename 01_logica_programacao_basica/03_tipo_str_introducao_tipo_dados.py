"""
Python = Linguagem de programação 
Tipo de tipagem = Dinâmica / forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)
# Aspas simples
print('"Paulo" Carvalho')
print(1, 'Paulo \'Carvalho')

# Aspas duplas
print("'Davi' Carvalho")
print("'Davi' \"Carvalho")

# Escape
print(r"'Davi' \"Carvalho\"")

# r
print(r"'Davi' \"Carvalho\"")
