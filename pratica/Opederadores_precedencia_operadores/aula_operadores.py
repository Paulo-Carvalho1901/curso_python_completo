A = float(input('Digite o primeiro numero: '))
B = float(input('Digite o segundo numero: '))

soma = A + B
substracao = A - B
multiplicacao = A * B
divisao = A / B
divisao_inteira = A % B

print('Resultados')
print('Soma: ', soma)
print('substracao: ', substracao)
print('multiplicacao: ', multiplicacao)
print('divisao: ', divisao)
print('divisao_inteira', divisao_inteira)
