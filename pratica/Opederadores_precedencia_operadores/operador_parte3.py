# Operadores de comparação

# > maior
# < menor
# == igual
# != diferente
# >= maior ou igual
# <= menor ou igual

#EX 1
numero_1 = 2
numero_2 = 4

soma = numero_1 + numero_2

if soma < 10:
    print("soma não é maior que 10")
else:
    print("soma é maior ou igual a 10")

# EX2

soma_1 = 7 + 8
soma_2 = 10 + 5

if soma_1 == soma_2:
    print("Os resultados são iguais")
else:
    print("Os resultados são diferentes")